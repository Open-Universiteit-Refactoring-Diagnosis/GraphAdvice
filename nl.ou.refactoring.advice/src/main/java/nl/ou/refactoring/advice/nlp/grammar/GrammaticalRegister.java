package nl.ou.refactoring.advice.nlp.grammar;

public enum GrammaticalRegister {
	/**
	 * The deferential register (e.g. 하소서체, hasoseoche in Korean).
	 */
	DEFERENTIAL,
	
	/**
	 * The high honorific register (e.g. 존댓말, jondaetmal in Korean).
	 */
	HIGH_HONORIFIC,
	
	/**
	 * The polite register (e.g. 해라체, haerache in Korean).
	 */
	POLITE,
	
	/**
	 * The casual register (e.g. 반말, banmal in Korean).
	 */
	CASUAL,
	
	/**
	 * The plain register (e.g. 해라체, haerache).
	 */
	PLAIN
}
