package nl.ou.refactoring.advice.nlp.grammar.verbs;

/**
 * The modality of a verb.
 */
public enum VerbModality {
	/**
	 * Statement of fact.
	 */
	INDICATIVE,
	
	/**
	 * Expresses doubt, desire or necessity.
	 */
	SUBJUNCTIVE,
	
	/**
	 * A command or a request.
	 */
	IMPERATIVE,
	
	/**
	 * A hypothetical situation.
	 */
	CONDITIONAL,
	
	/**
	 * A wish or a hope.
	 */
	OPTATIVE
}
