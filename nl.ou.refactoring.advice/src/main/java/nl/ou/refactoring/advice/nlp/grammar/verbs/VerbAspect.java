package nl.ou.refactoring.advice.nlp.grammar.verbs;

/**
 * The aspect of a verb.
 */
public enum VerbAspect {
	/**
	 * A completed action.
	 */
	PERFECTIVE,
	
	/**
	 * Ongoing or repeating action.
	 */
	IMPERFECTIVE,
	
	/**
	 * An action in progress, such as "-ing" verb conjugations in English.
	 */
	PROGRESSIVE
}
