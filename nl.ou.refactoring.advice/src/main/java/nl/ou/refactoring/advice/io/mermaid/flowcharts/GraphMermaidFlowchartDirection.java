package nl.ou.refactoring.advice.io.mermaid.flowcharts;

/**
 * A Mermaid flowchart direction.
 */
public enum GraphMermaidFlowchartDirection {
	/**
	 * The flowchart will be drawn from left to right.
	 */
	LeftToRight,
	
	/**
	 * The flowchart will be drawn from right to left.
	 */
	RightToLeft,
	
	/**
	 * The flowchart will be drawn from bottom to top.
	 */
	BottomToTop,
	
	/**
	 * The flowchart will be drawn from top to bottom.
	 */
	TopToBottom
}
