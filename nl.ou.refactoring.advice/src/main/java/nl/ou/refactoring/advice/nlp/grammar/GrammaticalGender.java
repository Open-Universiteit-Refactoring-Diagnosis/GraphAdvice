package nl.ou.refactoring.advice.nlp.grammar;

/**
 * A grammatical gender.
 */
public enum GrammaticalGender {
	/**
	 * Unknown grammatical gender.
	 */
	UNKNOWN,
	
	/**
	 * Feminine grammatical gender ("she"/"her").
	 */
	FEMININE,
	
	/**
	 * Masculine grammatical gender ("he"/"his").
	 */
	MASCULINE,
	
	/**
	 * Neuter grammatical gender ("it"/"they").
	 */
	NEUTER
}
